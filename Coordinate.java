package FinderBots;


public class Coordinate {

    double x = 0;
    double y = 0;

    public Coordinate(double x, double y) {

        this.x = x;
        this.y = y;



    }

    public double getX() {
        return this.x;

    }

    public double getY() {
        return this.y;
    }



}
